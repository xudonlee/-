package com.itzl.pojo;

import java.util.Date;

import org.springframework.context.annotation.Scope;
import org.springframework.format.annotation.DateTimeFormat;
import org.springframework.stereotype.Component;
@Component
@Scope("prototype")
public class Query {

	
	
		private Integer qNewsId;
		private String qNewsTitle;
		private String qNewsEditor;
		private String qNewsText;
		private String qNewsPic;
		@DateTimeFormat(pattern="yyyy-MM-dd")
		private Date qNewsDateStart;
		@DateTimeFormat(pattern="yyyy-MM-dd")
		private Date qNewsDateEnd;
		public Integer getqNewsId() {
			return qNewsId;
		}
		public void setqNewsId(Integer qNewsId) {
			this.qNewsId = qNewsId;
		}
		public String getqNewsTitle() {
			return qNewsTitle;
		}
		public void setqNewsTitle(String qNewsTitle) {
			this.qNewsTitle = qNewsTitle;
		}
		public String getqNewsEditor() {
			return qNewsEditor;
		}
		public void setqNewsEditor(String qNewsEditor) {
			this.qNewsEditor = qNewsEditor;
		}
		public String getqNewsText() {
			return qNewsText;
		}
		public void setqNewsText(String qNewsText) {
			this.qNewsText = qNewsText;
		}
		public String getqNewsPic() {
			return qNewsPic;
		}
		public void setqNewsPic(String qNewsPic) {
			this.qNewsPic = qNewsPic;
		}
		public Date getqNewsDateStart() {
			return qNewsDateStart;
		}
		public void setqNewsDateStart(Date qNewsDateStart) {
			this.qNewsDateStart = qNewsDateStart;
		}
		public Date getqNewsDateEnd() {
			return qNewsDateEnd;
		}
		public void setqNewsDateEnd(Date qNewsDateEnd) {
			this.qNewsDateEnd = qNewsDateEnd;
		}
	

}
